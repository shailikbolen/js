const month="march"
switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("February");
        break;
    case "march":
        console.log("March");
        break;
    case "apr":
        console.log("April");
        break;

    default:
        console.log("default case match");
        break;
}

//break is used to break the control flow
